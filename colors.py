def print_red(text):
    print("\033[31m" + text + "\033[0m")


def print_green(text):
    print("\033[32m" + text + "\033[0m")


def print_blue(text):
    print("\033[34m" + text + "\033[0m")


def print_magenta(text):
    print("\033[35m" + text + "\033[0m")


def print_yellow(text):
    print("\033[33m" + text + "\033[0m")
